st.write("Enter the patient details below to predict heart attack risk.")

# User Inputs
age = st.number_input("Age", min_value=1, max_value=120)

sex = st.selectbox(
    "Sex",
    options=[0, 1],
    format_func=lambda x: "Female" if x == 0 else "Male"
)

cp = st.number_input("Chest Pain Type (0-3)", min_value=0, max_value=3)

trestbps = st.number_input("Resting Blood Pressure")

chol = st.number_input("Cholesterol")

fbs = st.selectbox(
    "Fasting Blood Sugar > 120 mg/dl",
    options=[0, 1],
    format_func=lambda x: "No" if x == 0 else "Yes"
)

restecg = st.number_input("Rest ECG (0-2)", min_value=0, max_value=2)

thalach = st.number_input("Maximum Heart Rate")

exang = st.selectbox(
    "Exercise Induced Angina",
    options=[0, 1],
    format_func=lambda x: "No" if x == 0 else "Yes"
)

oldpeak = st.number_input("Oldpeak", format="%.1f")

slope = st.number_input("Slope (0-2)", min_value=0, max_value=2)

ca = st.number_input("Number of Major Vessels (0-4)", min_value=0, max_value=4)

thal = st.number_input("Thal (0-3)", min_value=0, max_value=3)
st.divider()
st.subheader("⚖️ BMI Calculator")

col1, col2 = st.columns(2)

with col1:
    height = st.number_input(
        "Height (cm)",
        min_value=50.0,
        max_value=250.0,
        value=170.0
    )

with col2:
    weight = st.number_input(
        "Weight (kg)",
        min_value=10.0,
        max_value=300.0,
        value=70.0
    )

bmi = weight / ((height / 100) ** 2)

st.metric("BMI", f"{bmi:.1f}")

if bmi < 18.5:
    st.info("⚠️ Underweight")
elif bmi < 25:
    st.success("✅ Normal Weight")
elif bmi < 30:
    st.warning("⚠️ Overweight")
else:
    st.error("🚨 Obese")
st.divider()

st.subheader("💧 Daily Water Intake")

water_intake = weight * 0.033

st.metric("Recommended Water Intake", f"{water_intake:.1f} Litres/day")

if water_intake < 2:
    st.info("💧 Drink at least 2 litres of water every day.")
else:
    st.success(f"💧 Recommended daily water intake: {water_intake:.1f} Litres")


# Prediction Button
if st.button("Predict"):

    input_data = np.array([[age, sex, cp, trestbps,
                            chol, fbs, restecg,
                            thalach, exang,
                            oldpeak, slope,
                            ca, thal]])

    prediction = model.predict(input_data)

    probability = model.predict_proba(input_data)[0]

    if prediction[0] == 1:
        confidence = probability[1] * 100
    else:
        confidence = probability[0] * 100

    st.divider()

    st.subheader("❤️ Prediction Result")
    st.subheader("📈 Risk Meter")

    risk_percentage = confidence if prediction[0] == 1 else (100 - confidence)

    st.progress(int(risk_percentage))

    st.write(f"**Risk Level:** {risk_percentage:.1f}%")

    if risk_percentage < 30:
        st.success("🟢 Low Risk")
    elif risk_percentage < 70:
        st.warning("🟡 Moderate Risk")
    else:
        st.error("🔴 High Risk")
    st.write("")    
    st.divider()

    st.subheader("📋 Patient Summary")

    gender = "Male" if sex == 1 else "Female"
    fbs_status = "Yes" if fbs == 1 else "No"
    angina = "Yes" if exang == 1 else "No"

