# Load the trained model
model = joblib.load('heart_attack_model.pkl')


def create_pdf(age, gender, prediction_text, confidence,
               health_score, bmi, water_intake):

    pdf = FPDF()
    pdf.add_page()

    # Title
    pdf.set_font("Arial", "", 11)
    pdf.cell(0, 8, f"Generated on: {datetime.now().strftime('%d-%m-%Y %I:%M %p')}", ln=True)
    pdf.ln(5)
    pdf.set_font("Arial", "B", 20)
    pdf.cell(0, 12, "HeartCare AI Report", ln=True, align="C")

    pdf.ln(8)

    pdf.set_font("Arial", "B", 14)
    pdf.cell(0, 10, "Patient Details", ln=True)

    pdf.set_font("Arial", "", 12)
    pdf.cell(0, 8, f"Age: {age}", ln=True)
    pdf.cell(0, 8, f"Gender: {gender}", ln=True)
    pdf.cell(0, 8, f"Prediction: {prediction_text}", ln=True)
    pdf.cell(0, 8, f"Confidence: {confidence:.2f}%", ln=True)
    pdf.cell(0, 8, f"Heart Health Score: {health_score}/100", ln=True)
    pdf.cell(0, 8, f"BMI: {bmi:.1f}", ln=True)
    pdf.cell(0, 8, f"Recommended Water Intake: {water_intake:.1f} L/day", ln=True)

    pdf.ln(8)

    # Diet Recommendation
    pdf.set_font("Arial", "B", 14)
    pdf.cell(0, 10, "Diet Recommendation", ln=True)

    pdf.set_font("Arial", "", 12)

    diet = [
        "- Eat more fruits and vegetables",
        "- Choose whole grains",
        "- Eat lean proteins like fish and chicken",
        "- Drink plenty of water",
        "- Avoid oily foods",
        "- Reduce salt intake",
        "- Avoid sugary drinks",
        "- Limit processed foods"
    ]

    for item in diet:
        pdf.cell(0, 8, item, ln=True)

    pdf.ln(5)

    # Exercise Recommendation
    pdf.set_font("Arial", "B", 14)
    pdf.cell(0, 10, "Exercise Recommendation", ln=True)

    pdf.set_font("Arial", "", 12)

    exercise = [
        "- Walk 30 minutes daily",
        "- Perform light stretching",
        "- Do yoga or meditation",
        "- Avoid prolonged sitting",
        "- Sleep 7-8 hours daily"
    ]

    for item in exercise:
        pdf.cell(0, 8, item, ln=True)

    pdf.ln(5)

    # Heart Health Tips
    pdf.set_font("Arial", "B", 14)
    pdf.cell(0, 10, "Heart Health Tips", ln=True)

    pdf.set_font("Arial", "", 12)

    tips = [
        "- Monitor blood pressure regularly",
        "- Maintain healthy body weight",
        "- Avoid smoking",
        "- Limit alcohol consumption",
        "- Take medicines as prescribed",
        "- Visit your doctor regularly"
    ]

    for item in tips:
        pdf.cell(0, 8, item, ln=True)

    pdf.ln(10)

    pdf.set_font("Arial", "I", 11)
    pdf.multi_cell(
        0,
        7,
        "This report is generated by HeartCare AI for educational purposes only and should not replace professional medical advice."
    )

    pdf.output("HeartCare_Report.pdf")


